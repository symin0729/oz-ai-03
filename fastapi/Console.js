// 1. 숫자 2개를 변수로 선언함
let a = 6;
let b = 2;

// 2. 연산자를 문자열로 정함("+", "-", "*", "/")
let operator = "/"

// 3. 연산자에 따라 계산 결과를 출력한다
if (operator === "+") {
  console.log(a + b);
} else if (operator === "-") {
  console.log(a - b);
} else if (operator === "*") {
  console.log(a * b);
} else if (operator === "/") {
  console.log(a / b);
} else {
  console.log("지원하지 않는 연산자입니다.");
}



